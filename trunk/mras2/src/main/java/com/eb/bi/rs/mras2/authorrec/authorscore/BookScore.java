package com.eb.bi.rs.mras2.authorrec.authorscore;

import java.util.ArrayList;

/**
 * Created by admin on 2016/3/4.
 */
public class BookScore {
    public int bookNum;
    public ArrayList<Double> scoreList;

    public BookScore(){
        bookNum = 0;
        scoreList = new ArrayList<Double>();
    }

    public BookScore(int bookNum,ArrayList<Double> scoreList){
        this.bookNum = bookNum;
        this.scoreList = new ArrayList<Double>();
        this.scoreList.addAll(scoreList);
    }
}
